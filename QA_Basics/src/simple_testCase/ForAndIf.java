package simple_testCase;

public class ForAndIf {

	public static void main(String[] args) {
   
		
		int i =10;
		int j= 20;
		if(j<i) {
			System.out.println("The output is Correct and Test Case is Passed ");
		}
		else {
			System.out.println("Wrong OutPut and Test Case is Failed");
			//initialize, condition and increament
			for(j=20; j>=0;j--) {
				System.out.println(j);
		   
			for( i = 1; i <= 20; i++) {
			 
				System.out.println(i);
			}
			}
			
		}
	}
}

